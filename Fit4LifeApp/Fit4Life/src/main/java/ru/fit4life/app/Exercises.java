package ru.fit4life.app;

public class Exercises {

    String icon = null;
    String name = null;
    String description = null;
    String technique = null;
    String working_muscles = null;
    String exercise_type = null;

    public String getIcon() {
        return icon;
    }
    public void setIcon(String icon) {
        this.icon = icon;
    }

    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }

    public String getDescription() {
        return description;
    }
    public void setDescription(String description) {
        this.description = description;
    }

    public String getTechnique() {
        return technique;
    }
    public void setTechnique(String technique) {
        this.technique = technique;
    }

    public String getWorkingMuscles() {
        return working_muscles;
    }
    public void setWorkingMuscles(String working_muscles) {
        this.working_muscles = working_muscles;
    }

    public String getExerciseType() {
        return exercise_type;
    }
    public void setExerciseType(String exercise_type) {
        this.exercise_type = exercise_type;
    }
}

